const allLanguages = document.querySelectorAll('.lang-img');
    logo = document.querySelector('.language-logo');
    title = document.querySelector('.language-title');
    paragraph = document.querySelector('.language-paragraph');
    themmeToggle = document.querySelector('.themme-toggle');
    links = document.querySelectorAll('a');
    main = document.querySelector('.main');
    header = document.querySelector('.header');
let allLang = ['ua', 'en', 'fr', 'pl', 'jp', 'nl', 'sa', 'lt', 'pt', 'cn', 'hu', 'es'];
let lang = 'en';


allLanguages.forEach((language) => {
    language.addEventListener('click', (e) => {
        lang = language.alt;
        changeURL();
    });
});

function changeURL(){
    location.href = window.location.pathname + '#' + lang;
    changeLang();
}

function changeLang(){
    let hash = (window.location.hash).substring(1);

    if(!allLang.includes(hash)){
        lang = 'en';
        changeURL();
    }else{
        lang = hash;
    }

    for(let key in semanticCore){
        document.querySelector('.language-' + key).innerHTML = semanticCore[key][lang];
    }
}

changeLang();

function changeColor(color, bgColor) {
    header.style.backgroundColor = bgColor;
    main.style.backgroundColor = bgColor;
    logo.style.color = color;
    title.style.color = color;
    paragraph.style.color = color;
    links.forEach(link => link.style.color = color);
}

function applyTheme(isDark) {
    if (isDark) {
        themmeToggle.classList.add('active');
        changeColor('#fff', '#000');
        localStorage.setItem('isDark', 'true');
    } else {
        themmeToggle.classList.remove('active');
        changeColor('#000', '#f4eae0');
        localStorage.setItem('isDark', 'false');
    }
}

window.addEventListener('DOMContentLoaded', () => {
    const isDark = localStorage.getItem('isDark') === 'true';
    applyTheme(isDark);
});

themmeToggle.addEventListener('click', () => {
    const isDark = localStorage.getItem('isDark') === 'true';
    applyTheme(!isDark);
});